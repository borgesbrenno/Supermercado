    private void btnSalvarComprasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarComprasActionPerformed
        double precoMorango = 10.0;
        double precoAmeixa = 8.0;
        double precoCoca = 10.0;
        double preco7belo = 10.0;
        double precoBisnaga = 8.9;
        double precoCarne = 25.0;
        double precoDesinfetante = 15.0;
        double precoDetergente = 6.0;
        double precoFini = 5.0;
        double precoFrango = 17.0;
        double precoGuarana = 10.0;
        double precoPao = 6.0;

        int quantidadeMorango = minhasCompras.adicionarUmMorango();
        int quantidadeAmeixa = minhasCompras.adicionarUmAmeixa();
        int quantidadeCoca = minhasCompras.adicionarUmCoca();
        int quantidade7belo = minhasCompras.adicionarUm7belo();
        int quantidadeBisnaga = minhasCompras.adicionarUmBisnaga();
        int quantidadeCarne = minhasCompras.adicionarUmCarne();
        int quantidadeDesinfetante = minhasCompras.adicionarUmDesinfetante();
        int quantidadeDetergente = minhasCompras.adicionarUmDetergente();
        int quantidadeFini = minhasCompras.adicionarUmFini();
        int quantidadeFrango = minhasCompras.adicionarUmFrango();
        int quantidadeGuarana = minhasCompras.adicionarUmGuarana();
        int quantidadePao = minhasCompras.adicionarUmPao();
// Solicitar que o cliente insira o CPF
        String cpfCliente = JOptionPane.showInputDialog("Digite o CPF:");

        // Verificar se o CPF Ã© vÃ¡lido (vocÃª pode adicionar validaÃ§Ãµes adicionais conforme necessÃ¡rio)
        if (cpfCliente != null && !cpfCliente.isEmpty()) {
            // Adicionar os produtos ao carrinho
            adicionarProdutoAoCarrinho(cpfCliente, "Morango", quantidadeMorango, precoMorango);
            adicionarProdutoAoCarrinho(cpfCliente, "Ameixa", quantidadeAmeixa, precoAmeixa);
            adicionarProdutoAoCarrinho(cpfCliente, "Coca", quantidadeCoca, precoCoca);
            adicionarProdutoAoCarrinho(cpfCliente, "7belo", quantidade7belo, preco7belo);
            adicionarProdutoAoCarrinho(cpfCliente, "Bisnaga", quantidadeBisnaga, precoBisnaga);
            adicionarProdutoAoCarrinho(cpfCliente, "Carne", quantidadeCarne, precoCarne);
            adicionarProdutoAoCarrinho(cpfCliente, "Desinfetante", quantidadeDesinfetante, precoDesinfetante);
            adicionarProdutoAoCarrinho(cpfCliente, "Detergente", quantidadeDetergente, precoDetergente);
            adicionarProdutoAoCarrinho(cpfCliente, "Fini", quantidadeFini, precoFini);
            adicionarProdutoAoCarrinho(cpfCliente, "Frango", quantidadeFrango, precoFrango);
            adicionarProdutoAoCarrinho(cpfCliente, "Guarana", quantidadeGuarana, precoGuarana);
            adicionarProdutoAoCarrinho(cpfCliente, "Pao", quantidadePao, precoPao);

            // Valor total da compra
            double valorTotal = calcularValorTotal();
            lblValorTotal.setText(String.valueOf(valorTotal));

            // Atualizar o texto do JLabel com o valor total
            lblValorTotal.setText(String.valueOf(valorTotal));

        } else {
            JOptionPane.showMessageDialog(null, "CPF invÃ¡lido. A compra nÃ£o serÃ¡ registrada.");

            // Limpa as quantidades apÃ³s salvar
            lblQuantidade.setText("0");
            lblQuantidadeAmeixa.setText("0");
            lblQuantidadeCoca.setText("0");
            lblQuantidadeDetergente.setText("0");
            lblQuantidadeGuarana.setText("0");
            lblQuantidadeDesinfetante.setText("0");
            lblQuantidadeFini.setText("0");
            lblQuantidade7belo.setText("0");
            lblQuantidadeCarne.setText("0");
            lblQuantidadeFrango.setText("0");
            lblQuantidadePao.setText("0");
            lblQuantidadeBisnaga.setText("0");

            if (carrinhoFrame != null) {
                carrinhoFrame.atualizarTabela(produtosSalvos);
            }

            // Carregar produtos do banco antes de atualizar o carrinho
            produtosSalvos = obterProdutosDoBanco(cpfCliente);

            // Atualizar a lista de produtos no carrinho
            atualizarCarrinho(carrinhoFrame.getTableCarrinho());
        }
    }//GEN-LAST:event_btnSalvarComprasActionPerformed

