    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed

        String login = txtLogin.getText();
        char senha[] = txtSenha.getPassword();
        
        if(verificaLoginAdm(login, senha)){
                    // Se forem válidos, abra a tela principal
                    JOptionPane.showMessageDialog(null, "Administrador Logado");
                    TelaPrincipal tela = new TelaPrincipal();
                    tela.setVisible(true);
                    
                    // Feche tela de login
                    TelaDeLogin.this.dispose();
                }
        else {
                    // Se não forem válidos, exiba uma mensagem de erro
                    JOptionPane.showMessageDialog(null, "Nome de usuário ou senha inválidos");
                }
        
        this.dispose();
        // TODO add your handling code here:
    }//GEN-LAST:event_btnConfirmarActionPerformed

    private void txtSenhaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSenhaKeyTyped
        char c = evt.getKeyChar();

        //Se o valor não estiver entre 0 e 9 ou for um backspace, eu vou cancelar a entrada
        if ((c < '0' || c > '9') && c != KeyEvent.VK_BACK_SPACE) {
            evt.consume();
        }
// TODO add your handling code here:

    }//GEN-LAST:event_txtSenhaKeyTyped

