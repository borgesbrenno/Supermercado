    private boolean verificaLoginAdm(String login, char[] senha) {
    // As credenciais de login válidas para um administrador são "admin" e "123456"
    
    String senhaString = String.valueOf(senha);
    
    return login.equals("admin") && senhaString.equals("123456");
}
